    private void cargarRecursosPrivados() {

        tableModel.setRowCount(0);
        ServicioRecursos servicio = new ServicioRecursos();
        try {
            java.util.List<Recurso> recursos = servicio.obtenerRecursosPrivados(this.usuarioLogueado);
            if (recursos.isEmpty()) {
                 tableModel.addRow(new Object[]{"No tienes recursos privados", "", ""});
            } else {
                for (Recurso r : recursos) {
                    Object[] fila = new Object[3];
                    fila[0] = r.getTitulo();
                    fila[1] = r.getTipoArchivo();
                    fila[2] = r.getFechaPublicacion().toString();
                    tableModel.addRow(fila);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar recursos privados: " + e.getMessage());
        }
    }

