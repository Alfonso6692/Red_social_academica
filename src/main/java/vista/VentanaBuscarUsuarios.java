    private void botonEnviarSolicitudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEnviarSolicitudActionPerformed
        int indiceSeleccionado = listaUsuarios.getSelectedIndex();

    // Verificar que un usuario haya sido seleccionado de la lista
    if (indiceSeleccionado != -1) {
        try {
            // Obtener el objeto Usuario completo de la lista de respaldo
            Usuario destinatario = this.listaDeUsuariosCompleta.get(indiceSeleccionado);

            // Llamar al servicio de conexiones para enviar la solicitud
            ServiciosConexiones servicioConexiones = new ServiciosConexiones();
            servicioConexiones.enviarSolicitud(this.usuarioLogueado, destinatario);

            JOptionPane.showMessageDialog(this, "Solicitud de conexi√≥n enviada a " + destinatario.getNombre());

        } catch (Exception e) {
            // Manejar posibles errores, como enviar una solicitud duplicada
            if (e.getMessage().contains("duplicada")) {
                JOptionPane.showMessageDialog(this, "Ya has enviado una solicitud a este usuario.", "Error", JOptionPane.WARNING_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Error al enviar la solicitud: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Por favor, selecciona un usuario de la lista.");
    }
        
        
        
    }//GEN-LAST:event_botonEnviarSolicitudActionPerformed

